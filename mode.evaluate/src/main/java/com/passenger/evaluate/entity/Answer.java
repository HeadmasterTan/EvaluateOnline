package com.passenger.evaluate.entity;

public class Answer {
	private String subjectid;
	private String answerid;
	private String contendesc;
	private Float wghvalue;
	public String getSubjectid() {
		return subjectid;
	}
	public void setSubjectid(String subjectid) {
		this.subjectid = subjectid;
	}
	public String getAnswerid() {
		return answerid;
	}
	public void setAnswerid(String answerid) {
		this.answerid = answerid;
	}
	public String getContendesc() {
		return contendesc;
	}
	public void setContendesc(String contendesc) {
		this.contendesc = contendesc;
	}
	public Float getWghvalue() {
		return wghvalue;
	}
	public void setWghvalue(Float wghvalue) {
		this.wghvalue = wghvalue;
	}
	
}
